/**
 * Восстанавливает «ё» в тексте на основе словаря.
 * Сохраняет регистр оригинала (заглавные/строчные).
 *
 * @returns { text: новый текст, replacements: число замен }
 */
export function recoverYo(text: string): { text: string; replacements: number } {
  if (!text) return { text, replacements: 0 };

  let count = 0;
  // Разбиваем по словам, сохраняя пробелы и пунктуацию
  const result = text.replace(/[а-яёА-ЯЁ]+/g, (word) => {
    const lower = word.toLowerCase();

    // Пропускаем омографы — они требуют контекста
    if (OMOGRAPHS.has(lower)) return word;

    const replacement = YO_DICT[lower];
    if (!replacement || replacement === lower) return word;

    count++;
    return preserveCase(word, replacement);
  });

  return { text: result, replacements: count };
}

/**
 * Сохраняет паттерн регистра исходного слова в замене.
 * "ВСЁ" → "ВСЁ", "Всё" → "Всё", "всё" → "всё".
 */
function preserveCase(original: string, replacement: string): string {
  if (original === original.toUpperCase()) return replacement.toUpperCase();
  if (original[0] === original[0].toUpperCase()) {
    return replacement[0].toUpperCase() + replacement.slice(1);
  }
  return replacement;
}

// ── Омографы — слова, где «е»/«ё» зависит от смысла ────────
// Не заменяем автоматически — нужен контекст.
const OMOGRAPHS = new Set<string>([
  "все",      // все люди / всё хорошо
  "всё",
  "небо",     // небо / нёбо (анатом.)
  "нёбо",
  "осел",     // от «осесть» / осёл (животное)
  "осёл",
  "узнает",   // узнает (буд.) / узнаёт (наст.)
  "узнаёт",
  "падеж",    // грам. падеж / падёж скота
  "падёж",
  "белок",
  "белёк",
]);

// ── Авто-генерация словаря { без_ё → с_ё } ─────────────────
const YO_DICT: Map<string, string> = (() => {
  const map = new Map<string, string>();
  for (const yoWord of YO_WORDS) {
    const plain = yoWord.replace(/ё/g, "е").replace(/Ё/g, "Е");
    if (plain === yoWord) continue;            // нет «ё» — пропускаем
    if (OMOGRAPHS.has(plain)) continue;        // омограф — не заменяем
    if (map.has(plain) && map.get(plain) !== yoWord) {
      // Конфликт двух разных «ё»-вариантов на одно «е»-слово — пропускаем оба.
      map.delete(plain);
      continue;
    }
    map.set(plain, yoWord);
  }
  return map;
})();

// ── Public API ──────────────────────────────────────────────

/**
 * Восстанавливает «ё» в тексте на основе словаря.
 * Сохраняет регистр оригинала (заглавные/строчные/CamelCase).
 *
 * @returns { text: новый текст, replacements: число замен }
 */
export function recoverYo(text: string): { text: string; replacements: number } {
  if (!text) return { text, replacements: 0 };

  let count = 0;
  const result = text.replace(/[а-яёА-ЯЁ]+/g, (word) => {
    const lower = word.toLowerCase();
    if (OMOGRAPHS.has(lower)) return word;
    const replacement = YO_DICT.get(lower);
    if (!replacement) return word;
    count++;
    return preserveCase(word, replacement);
  });

  return { text: result, replacements: count };
}

/**
 * Сохраняет паттерн регистра исходного слова в замене.
 * "ВСЁ" → "ВСЁ", "Всё" → "Всё", "всё" → "всё".
 */
function preserveCase(original: string, replacement: string): string {
  if (original === original.toUpperCase()) return replacement.toUpperCase();
  if (original[0] === original[0].toUpperCase()) {
    return replacement[0].toUpperCase() + replacement.slice(1);
  }
  return replacement;
}


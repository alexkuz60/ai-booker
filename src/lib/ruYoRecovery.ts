// ── Омографы — слова, где «е»/«ё» зависит от смысла ────────
// НЕ заменяем автоматически. Возможно, в будущем — диалог уточнения.
const OMOGRAPHS = new Set([
  "все", "всё",       // все люди / всё хорошо
  "небо", "нёбо",     // небо / нёбо (анатом.)
  "осел", "осёл",     // осел (от «осесть») / осёл (животное)
  "узнает", "узнаёт", // он узнает (буд.) / он узнаёт (наст.)
  "падеж", "падёж",   // грам. падеж / падёж скота
  "белок", "белёк",   // белок / детёныш тюленя
]);

